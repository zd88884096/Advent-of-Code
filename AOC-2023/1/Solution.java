    public static int letters(String S, int i){
        String[] l = {"one", "two", "three", "four", "five", "six", "seven", "eight", "nine"};
        for(int ind = 0; ind < 9; ++ind){
            String candidate = l[ind];
            if(i + candidate.length() <= S.length() && S.substring(i, i + candidate.length()).equals(candidate)){
                return ind + 1;
            }
        }
        return -1;
    }
    public static void main(String[] args){
        sc = new BufferedReader(new InputStreamReader(System.in));
        int sum = 0;
        while(true){
            String S = read_toks()[0];
            char[] s = S.toCharArray();
            int fs = -1, sc = -1;
            if(s.length == 0){
                break;
            }
            for(int i = 0; i < s.length; ++i){
                char c = s[i];
                if(c >= '0' && c <= '9'){
                    if(fs == -1){
                        fs = c - '0';
                    }
                    sc = c - '0';
                }
                int letter = letters(S, i);
                if(letter != -1){
                    if(fs == -1){
                        fs = letter;
                    }
                    sc = letter;
                }
            }
            sum += 10 * fs + sc;
        }
        System.out.println(sum);
    }
