    public static void main(String[] args){
        //idea: brute force, check if the N characters starting from i-th character
        //  are all different

        //part I: set N = 4;
        int N = 14;
        String S = read_all_String()[0];
        for(int i = 0; i < S.length() - (N - 1); ++i){
            //if we add the N characters to a set, and set's size = N
            //  it means there are no repeating characters -> N unique characters
            if(setify(S.substring(i, i + N)).size() == N){
                System.out.println((i + N));
                return ;
            }
        }
    }
    

